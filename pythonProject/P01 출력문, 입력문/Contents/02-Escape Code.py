'''
이스케이프 코드는 문자열 출력에서 다음의 기능을 수행함.
\n : 줄바꿈(enter 역할, newline)
\t : 탭공백(tab 역할, 4칸 스페이스)
\' : 따옴표 표현
'''

print('한 줄 띄어서 \n출력해 봅시다.')
print('\t탭만큼 공백 주기')
print('문자열 안에서 \'따옴표\'를 넣으려면')
print("쌍따옴표(\")도 마찬가지")
print('역슬래시(\\\\)도 표현')

# 문자열 안에서의 다른 종류 따옴표는 문자취급
print('멘트: "배고프다"')
print("멘트: '배고프다'")
















